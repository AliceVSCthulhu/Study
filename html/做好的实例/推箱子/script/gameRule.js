import { correct, config } from "./mapInfo.js";
import { getBlock } from "./move.js";


export function isWin() {
  let isWin = true;
  for (const blockInfo of correct) {
    if (getBlock(blockInfo.row, blockInfo.col) !== config.BOX) {
      isWin = false;
    }
  }
  return isWin;
}

